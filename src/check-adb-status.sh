# ADB server port
ADB_SERVER_PORT="$1"

# ADB port
ADB_PORT="$2"

ADB_STATUS=`adb -P $ADB_SERVER_PORT -e connect localhost:$ADB_PORT | head -1`

if [[ $ADB_STATUS = unable* ]]; then
    exit 1
else
    exit 0
fi
